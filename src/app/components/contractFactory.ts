// Kết nối provider, signer, factory contract
export async function getProviderAndSigner() {
  if (!window.ethereum) throw new Error("MetaMask not found");
  const provider = new ethers.BrowserProvider(window.ethereum);
  await provider.send("eth_requestAccounts", []);
  const signer = await provider.getSigner();
  return { provider, signer };
}

export async function getFactoryContract() {
  const { signer } = await getProviderAndSigner();
  return new ethers.Contract(factoryAddress, factoryABI, signer);
}

export async function getVotingContract(address) {
  const { signer } = await getProviderAndSigner();
  return new ethers.Contract(address, votingABI, signer);
}